package Basics;

public class Program11 {
//11) Program to Convert Celsius To Fahrenheit. Hint: F= (C x 9/5)+32
	public static void main(String[] args) {
		float Celsius, Fah;
		Celsius = 6;
		//celsius to Fahrenheit
		Fah = (Celsius * 9/5)+32;
		System.out.println(Celsius+" "+"Celsius is"+" "+Fah+" "+"Fahrenheit");
		

	}

}
