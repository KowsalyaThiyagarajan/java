package Basics;

public class Program5 {
//5) Find the perimeter of a circle given the radius. Hint: (2 x Pi x r)
	public static void main(String[] args) {
	
		//declaring Variables
		float rad=5,pi=3.14f;
		
		//Formula for perimeter
		double circle = 2*pi*rad;
		
		//printing
		System.out.println("Perimeter of the Circle:"+circle);

	}

}
